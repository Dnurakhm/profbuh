-- ============================================
-- УДАЛЕНИЕ ДУБЛИРУЮЩИХСЯ ТРИГГЕРОВ
-- ============================================
-- Этот SQL нужно выполнить в Supabase SQL Editor
-- для удаления старых/дублирующихся триггеров

-- Удаляем старые триггеры для bids
DROP TRIGGER IF EXISTS on_new_bid_notification ON bids;
-- Оставляем только on_bid_insert_notification из новой системы

-- Удаляем старые триггеры для jobs (если есть дубликаты)
DROP TRIGGER IF EXISTS on_job_assigned ON jobs;
-- Оставляем только on_job_accountant_assigned из новой системы

-- ============================================
-- ПРИМЕЧАНИЯ:
-- ============================================
-- 1. После выполнения останется только один триггер для каждого типа:
--    - on_bid_insert_notification для bids
--    - on_job_accountant_assigned для jobs
--    - on_message_insert_notification для messages
-- 2. Все триггеры используют функции из новой системы уведомлений
